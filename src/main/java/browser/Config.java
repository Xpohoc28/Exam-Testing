package browser;

/**
 * Класс Config предназначен для хранения конфигурационных параметров браузера.
 * Используется для централизованного управления настройками тестового окружения.
 */
public class Config {

    /**
     * Константа BROWSER_TYPE определяет тип браузера, который будет использоваться в тестах.
     * Возможные значения:
     * - "chrome" - для Google Chrome
     * - "firefox" - для Mozilla Firefox
     * - "edge" - для Microsoft Edge
     * 
     * Использование константы вместо строковых литералов в коде:
     * 1. Упрощает изменение типа браузера в одном месте
     * 2. Предотвращает опечатки в названиях браузеров
     * 3. Упрощает поддержку кода
     */
    public static final String BROWSER_TYPE = "chrome";

}

